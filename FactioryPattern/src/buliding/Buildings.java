public class Buildings {

    String name ,power;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPower() {
        return power;
    }

    public void setPower(String power) {
        this.power = power;
    }

    public String toString() {
        return "name:"+name+" power :"+power;
    }

}
